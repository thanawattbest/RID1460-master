}

class _MyHomePageState extends State<MyHomePage> {
  int _counter = 0;

  void _incrementCounter() {
    setState(() {
      _counter++;
    });
  }

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(
        title: Text("testestest"),
      ),
      body: Container(
        color: Colors.red,
        height: MediaQuery.of(context).size.height * 0.5,
